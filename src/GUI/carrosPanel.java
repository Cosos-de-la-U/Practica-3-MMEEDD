    private void bnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnGenerarActionPerformed
        tablaCarros.getDataVector().removeAllElements();
        tablaCarros.fireTableDataChanged();
        Carros oCarros;
        Mantos oManto;
        for (int i = 0; i < spAutos.getValue().hashCode(); i++) {
            //LLamar para asignar los datos
            //CREAR NUEVOS OBJETOS Y NUEVA LISTA
            oCarros = new Carros();
            lMantos = new ArrayList<>();
            oCarros.setPlaca();
            oCarros.setModelo();
            oCarros.setAño();
            oCarros.setColor();
            //Crear los valores de mantos
            for (int j = 0; j < spMantos.getValue().hashCode(); j++) {
                oManto = new Mantos();
                oManto.setTipoManto();
                oManto.setPrecioManto();
                oManto.setFechaManto();
                //Guardarlos en Arraylist de Mantos
                lMantos.add(oManto);
            }
            //Guardar ya todo
            oCarros.setlMantenimientos(lMantos);
            lCarros.add(oCarros);
        }
        //Reproducirlos en la tabla
        for (int j = 0; j < lCarros.size(); j++) {
            String[] rowAutos = {
                lCarros.get(j).getPlaca(),
                lCarros.get(j).getModelo(),
                lCarros.get(j).getAño() + "",
                lCarros.get(j).getColor()
            };
            //Aqui se inscriben
            tablaCarros.addRow(rowAutos);
        }
    }//GEN-LAST:event_bnGenerarActionPerformed

    private void dataTablaMantos(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dataTablaMantos
        tablaMantos.getDataVector().removeAllElements();
        tablaMantos.fireTableDataChanged();
        int index = tbVehiculos.getSelectedRow();
        for (int i = 0; i < lCarros.get(index).getlMantenimientos().size(); i++) {
            String[] rowMantos = {
                lCarros.get(index).getlMantenimientos().get(i).getTipoManto(),
                Double.toString(lCarros.get(index).getlMantenimientos().get(i).getPrecioManto()),
                lCarros.get(index).getlMantenimientos().get(i).getFechaManto() + ""
            };
            tablaMantos.addRow(rowMantos);
        }
    }//GEN-LAST:event_dataTablaMantos

