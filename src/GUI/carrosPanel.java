    public void cargarTablas() {
        //Tabla Carros
        ArrayList<Object> lCabezaCarros = new ArrayList<>();
        lCabezaCarros.add("Placa");
        lCabezaCarros.add("Modelo");
        lCabezaCarros.add("Año");
        lCabezaCarros.add("Color");

        for (Object columns : lCabezaCarros) {
            tablaCarros.addColumn(columns);
        }
        tbVehiculos.setModel(tablaCarros);
        //Tabla Mantos
        ArrayList<Object> lCabezaMantos = new ArrayList<>();
        lCabezaMantos.add("Tipo");
        lCabezaMantos.add("Precio");
        lCabezaMantos.add("Fecha");
        for (Object columns : lCabezaMantos) {
            tablaMantos.addColumn(columns);
        }
        tbMantos.setModel(tablaMantos);

    }

    //RECARGAR TABLA CARROS
    private void recargarTablaCarros() {
        this.tablaCarros.setRowCount(0);
        for (int i = 0; i < this.lCarros.size(); i++) {
            String[] registro = {
                this.lCarros.get(i).getPlaca(),
                this.lCarros.get(i).getModelo(),
                this.lCarros.get(i).getAño() + "",
                this.lCarros.get(i).getColor()};
            tablaCarros.addRow(registro);
        }
    }

    //RECARGAR TABLA MANTOS
    private void recargarTablaMantos(int index) {
        this.tablaMantos.setRowCount(0);
        for (int i = 0; i < this.lCarros.get(index).getlMantenimientos().size(); i++) {
            String[] registro = {
                this.lCarros.get(index).getlMantenimientos().get(i).getTipoManto(),
                this.lCarros.get(index).getlMantenimientos().get(i).getPrecioManto() + "",
                this.lCarros.get(index).getlMantenimientos().get(i).getFechaManto() + ""
            };
            tablaMantos.addRow(registro);
        }
    }

